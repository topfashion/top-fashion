export default async function handler(req, res) {
  if (req.method !== 'POST') {
    return res.status(200).send('OK');
  }

  const body = req.body;
  if (!body || !body.message) {
    return res.status(200).send('OK');
  }

  const chatId = body.message.chat.id;
  const userText = body.message.text;

  if (!userText) {
    return res.status(200).send('OK');
  }

  const botToken = process.env.TELEGRAM_BOT_TOKEN;
  const openRouterApiKey = process.env.GEMINI_API_KEY;

  if (userText === '/start') {
    const welcomeMessage = "Привет! Я умный помощник детской модельной школы развития \"ТопФешен\". Задайте мне любой вопрос о направлениях обучения, ценах, расписании или о том, как подготовиться к занятиям!";
    await sendTelegramMessage(chatId, welcomeMessage, botToken);
    return res.status(200).send('OK');
  }

  try {
    const aiResponse = await getOpenRouterResponse(userText, openRouterApiKey);
    await sendTelegramMessage(chatId, aiResponse, botToken);
  } catch (error) {
    console.error(error);
    await sendTelegramMessage(chatId, "Извините, произошла техническая ошибка. Пожалуйста, попробуйте написать позже или свяжитесь с администратором школы.", botToken);
  }

  return res.status(200).send('OK');
}

